package Classes;

public class Coordinates {
	
	private Integer x;
	private Integer y;
	
	//Constructor 
	
	public Coordinates(Integer x, Integer y) {
		super();
		this.x = x;
		this.y = y;
	}
	
	
	// Getters And Setters

	public Integer getX() {
		return x;
	}

	public void setX(Integer x) {
		this.x = x;
	}

	public Integer getY() {
		return y;
	}

	public void setY(Integer y) {
		this.y = y;
	}
	
	
	
	
	
	
	

}
